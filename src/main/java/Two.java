public class Two {

    public static void add(int i, int j){
        int sum = i+j;
        System.out.println("Sum = "+ sum);
    }

    public static void multiply(int i, int j){
        int mul = i*j;
        System.out.println("Mult = "+ mul);
    }

    public static void subtract(int i, int j){
        int sub = i-j;
        System.out.println("Subtract = "+ sub);
    }
}
